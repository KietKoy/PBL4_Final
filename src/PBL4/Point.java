package PBL4;

public class Point {
	int x;
	int y;
	public Point(int x, int y)
	{
		this.x = x;
		this.y = y;
	}
	public void setX(int value)
	{
		this.x = value;
	}
	public void setY(int value)
	{
		this.y = value;
	}
	public int getX()
	{
		return this.x;
	}
	public int getY()
	{
		return this.y;
	}
}
